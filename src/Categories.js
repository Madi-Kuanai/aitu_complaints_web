export const findCategoryById = (id: number | string): Category | undefined => {
    for (const category of categories) {
        if (category.id === id) {
            return category;
        }

        if (category.subcategories) {
            const subcategory = category.subcategories.find(subcat => subcat.id === id);
            if (subcategory) {
                return subcategory;
            }
        }
    }
};